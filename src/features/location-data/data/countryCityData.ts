// For other countries not explicitly mapped, provide at least the capital city and Other option
export const getDefaultCities = (country: string): CityOption[] => {
  if (!country) return [{ value: "Other", label: "Other (specify)" }];
  
  const capital = countryCapitals[country];
  if (capital) {
    return [
      { value: capital, label: capital },
      { value: "Other", label: "Other (specify)" }
    ];
  }
  
  return [{ value: "Other", label: "Other (specify)" }];
};

/**
 * Get city options for a selected country
 * @param country The selected country
 * @returns Array of city options
 */
export const getCityOptions = (country: string): CityOption[] => {
  return countryCityMap[country] || getDefaultCities(country);
};

/**
 * Check if a city is "Other" and requires custom input
 * @param city The selected city value
 * @returns Boolean indicating if custom input is needed
 */
export const isCustomCityNeeded = (city: string): boolean => {
  return city === "Other";
};
